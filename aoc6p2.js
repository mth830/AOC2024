function dfs(i, j) {
  let dirSet= new Set();
  let set = new Set();
  let dIndex = 0;
  let inbounds = (x = i, y = j) => x >= 0 && x < list.length && y >= 0 && y < list[0].length;
  while (true) {
    let [a, b] = dirs[dIndex++ % 4];
    while (inbounds(i + a, j + b) && list[i + a][j + b] !== '#') {
      i += a;
      j += b;
      set.add([i, j].toString());
      let key =[i,j,dIndex%4].toString();
      if(dirSet.has(key)) return false;
      dirSet.add(key);
    }
    if (!inbounds(i + a, j + b)) return 1;
  }

}